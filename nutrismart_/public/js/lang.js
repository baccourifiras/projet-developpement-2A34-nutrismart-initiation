// ─── Langue active ───────────────────────────────────────────────
const LANG_STORAGE_KEY = 'nutrismart_lang';

function getNutriLang() {
  return localStorage.getItem(LANG_STORAGE_KEY) || 'fr';
}

function setNutriLang(code) {
  localStorage.setItem(LANG_STORAGE_KEY, code);
  applyNutriLang(code);
}

function t(key) {
  const lang = getNutriLang();
  const dict = NUTRISMART_LANGS[lang] || NUTRISMART_LANGS['fr'];
  return dict[key] || NUTRISMART_LANGS['fr'][key] || key;
}

// ─── Application des traductions ─────────────────────────────────
function applyNutriLang(code) {
  const lang = NUTRISMART_LANGS[code] || NUTRISMART_LANGS['fr'];

  // Direction du texte
  document.documentElement.lang = code;
  document.documentElement.dir  = lang.dir || 'ltr';

  // Tous les éléments avec data-i18n
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (lang[key] !== undefined) el.textContent = lang[key];
  });

  // Attributs placeholder
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (lang[key] !== undefined) el.placeholder = lang[key];
  });

  // Title de la page
  const pageKey = document.body.getAttribute('data-page-title');
  if (pageKey && lang[pageKey]) document.title = lang[pageKey];

  // Sélecteur de langue actif
  document.querySelectorAll('.lang-option').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === code);
  });
}

// ─── Rendu du sélecteur ──────────────────────────────────────────
function renderLangSwitcher() {
  const langs = [
    { code: 'fr', label: 'FR', flag: '🇫🇷' },
    { code: 'en', label: 'EN', flag: '🇬🇧' },
    { code: 'es', label: 'ES', flag: '🇪🇸' },
    { code: 'de', label: 'DE', flag: '🇩🇪' },
    { code: 'zh', label: '中文', flag: '🇨🇳' },
    { code: 'ar', label: 'AR', flag: '🇸🇦' },
  ];

  const current = getNutriLang();
  const currentLang = langs.find(l => l.code === current) || langs[0];

  return `
  <div class="lang-switcher" id="langSwitcher">
    <button class="lang-trigger" onclick="toggleLangMenu()" aria-label="Changer la langue">
      <span>${currentLang.flag}</span>
      <span class="lang-trigger-code">${currentLang.label}</span>
      <svg width="10" height="6" viewBox="0 0 10 6" fill="none" style="margin-left:2px;">
        <path d="M1 1L5 5L9 1" stroke="currentColor" stroke-width="1.5" stroke-linecap="round" stroke-linejoin="round"/>
      </svg>
    </button>
    <div class="lang-menu" id="langMenu">
      ${langs.map(l => `
        <button class="lang-option ${l.code === current ? 'active' : ''}"
                data-lang="${l.code}"
                onclick="setNutriLang('${l.code}');closeLangMenu();">
          <span>${l.flag}</span>
          <span>${l.label}</span>
        </button>
      `).join('')}
    </div>
  </div>`;
}

function toggleLangMenu() {
  const menu = document.getElementById('langMenu');
  if (menu) menu.classList.toggle('open');
}

function closeLangMenu() {
  const menu = document.getElementById('langMenu');
  if (menu) menu.classList.remove('open');
  // Re-render trigger label
  const switcher = document.getElementById('langSwitcher');
  if (switcher) {
    switcher.outerHTML = renderLangSwitcher();
  }
}

// Fermer en cliquant ailleurs
document.addEventListener('click', function(e) {
  const sw = document.getElementById('langSwitcher');
  if (sw && !sw.contains(e.target)) closeLangMenu();
});

// ─── Init ────────────────────────────────────────────────────────
document.addEventListener('DOMContentLoaded', function() {
  // Injecter le sélecteur dans toutes les navbars
  const navLinks = document.querySelector('.nav-links');
  if (navLinks) {
    const div = document.createElement('div');
    div.innerHTML = renderLangSwitcher();
    navLinks.insertBefore(div.firstElementChild, navLinks.firstChild);
  }

  // Appliquer la langue sauvegardée
  applyNutriLang(getNutriLang());
});
